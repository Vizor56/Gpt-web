CREATE TABLE IF NOT EXISTS student_friend_requests (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  requester_id INTEGER NOT NULL REFERENCES students(id) ON DELETE CASCADE,
  receiver_id INTEGER NOT NULL REFERENCES students(id) ON DELETE CASCADE,
  status TEXT NOT NULL DEFAULT 'Pending' CHECK (status IN ('Pending', 'Accepted', 'Declined', 'Cancelled')),
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CHECK (requester_id <> receiver_id),
  UNIQUE (requester_id, receiver_id)
);

CREATE TABLE IF NOT EXISTS student_friends (
  student_id INTEGER NOT NULL REFERENCES students(id) ON DELETE CASCADE,
  friend_id INTEGER NOT NULL REFERENCES students(id) ON DELETE CASCADE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  PRIMARY KEY (student_id, friend_id),
  CHECK (student_id <> friend_id)
);

CREATE TABLE IF NOT EXISTS curator_teachers (
